// Class to practice the concept of For Loops in Java
public class Java_Loops {
    
    public static void main(String[] args) {
        
        // Loop starts from i = 1 and runs until i becomes 10 (increments by 1 each time)
        for (int i = 1; i <= 10; i++) {
            
            // Prints the current value of i in each iteration
            System.out.println( + i);
        }
    }
}